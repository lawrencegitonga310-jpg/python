# comparison operators-They are used to compare two or more items and they ussually return a boolean answer

number1 = 2
number2 = 5

print("is number1 greater than number2?",number1>number2)

print("is number1 greater than number2?",number2 > number1)

print("is number1 greater than or equal to number2?",number1 >= number2)

print("is number1 less than or equal to number2?",number1 <= number2)

print("is number1 equal to number2?",number1==number2)

print("is number1 not equal to number2?",number1 != number2)

# logical operators-
# Logical AND
# it returns true if the condition /statement evaluates to true
print((3 > 1)and(7>2))
# logical or
# it evaluates to true if one of the condition / is true
print((3>1)or(7<6))

# logical not it is used to negate a statement/condition
print(not(90>70))
